package com.pluralsight;

public class Moped extends Vehicle {
    private boolean helmetStorage;
    private String scooterStyle;

    public boolean isHelmetStorage() {
        return helmetStorage;
    }

    public void setHelmetStorage(boolean helmetStorage) {
        this.helmetStorage = helmetStorage;
    }

    public String getScooterStyle() {
        return scooterStyle;
    }

    public void setScooterStyler(String scooterStyle) {
        this.scooterStyle = scooterStyle;
    }
}
